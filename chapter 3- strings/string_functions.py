story = "once upon a time there was a youtuber named harry who uploaded python course with notes upon"
# string functions
print(len(story)) #length of the sentence
print(story.endswith("notes")) #checks if it ends with notes
print(story.count("a")) #counts the occurance of a
print(story.count("upon"))  # counts the word upon
print(story.capitalize())  #capitalizes the sentences
print(story.find("harry")) #finds harry
print(story.find("sugam")) #finds the words sugam and if it gives -1 then it doesn't exists
print(story.find("upon")) #finds the 1st one only
print(story.replace("harry", "sugam")) #replaces the old word with new word

